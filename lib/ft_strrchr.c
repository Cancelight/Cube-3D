#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		i++;
	}
	if (c == '\0')
		return ((char *) s + i);
	while (i < i + 1)
	{
		if (s[i] == (char) c)
			return (((char *) s + i));
		i--;
	}
	return ((char *) NULL);
}
